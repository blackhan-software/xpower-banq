#!/usr/bin/env bash
###############################################################################
CMD_SCRIPT=$(cd "$(dirname "${BASH_SOURCE[0]}")" >/dev/null 2>&1 && pwd)
###############################################################################

PNG_PATH="${1:-/tmp}"
RHS_SEED="${2:-256}"
LHS_SEED="${3:-1}"

for SEED in $(seq $LHS_SEED $RHS_SEED) ; do
    CMD=$(echo "$CMD_SCRIPT/twap-ema.py" \
        --png-path "$PNG_PATH" \
        --random-seed $SEED) ;
    echo $CMD ; eval $CMD ;
done

###############################################################################
###############################################################################
